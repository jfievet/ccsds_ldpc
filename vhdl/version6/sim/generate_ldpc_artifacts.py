def emit_compile_all(configs: tuple[dict[str, int | str], ...], output_path: Path) -> None:
    lines = [
        "onerror {quit -code 1 -f}",
        "onbreak {quit -code 1 -f}",
        "",
        "set ::compile_all_running 1",
        "",
    ]
    for config in configs:
        suffix = str(config["suffix"])
        lines.append(f"puts \"Running compile_{suffix}.do\"")
        lines.append(f"do compile_{suffix}.do")
        lines.append("")
    lines.extend([
        "unset ::compile_all_running",
        "quit -f",
        "",
    ])
    output_path.write_text("\n".join(lines), encoding="ascii")


def main() -> int:
    args = parse_args()
    script_dir = Path(__file__).resolve().parent
    version_dir = script_dir.parent
    src_dir = version_dir / "src"
    tb_dir = version_dir / "tb"
    sim_dir = version_dir / "sim"

    requested_targets = set(args.targets)
    for config in CONFIGS:
        suffix = str(config["suffix"])
        if requested_targets and suffix not in requested_targets:
            continue

        info_length = int(config["info_length"])
        emit_config_package(config, src_dir / f"ldpc_encoder_{suffix}_config_pkg.vhd")
        emit_wrapper(config, src_dir / f"ldpc_encoder_{suffix}.vhd")
        emit_standard_testbench(config, tb_dir / f"tb_ldpc_encoder_{suffix}.vhd")
        emit_vunit_testbench(config, tb_dir / f"tb_vunit_ldpc_encoder_{suffix}.vhd")
        emit_wave_script(config, sim_dir / f"wave_{suffix}.do")
        emit_compile_script(config, sim_dir / f"compile_{suffix}.do")
        print(f"Generated static metadata files for {suffix}")

    emit_compile_all(CONFIGS, sim_dir / "compile_all.do")
    emit_compile_alias(sim_dir / "compile.do")
    emit_wave_alias(sim_dir / "wave.do")
    emit_run_py(sim_dir / "run.py")
    return 0


